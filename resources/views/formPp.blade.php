              if ((val >= 3 || val === 0) && foto_ktp && foto_ktp.files.length === 0) {
                  foto_ktp.style.borderColor = "red";
                  flag = false;
              } else if (foto_ktp) {
                  foto_ktp.style.borderColor = "green";
              }

              return flag;


